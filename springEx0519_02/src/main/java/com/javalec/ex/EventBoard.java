package com.javalec.ex;

public class EventBoard {
	
	private int e_num;
	private String e_title;
	private String e_beginningDay;
	private String e_endDay;
	
	public int getE_num() {
		return e_num;
	}
	public void setE_num(int e_num) {
		this.e_num = e_num;
	}
	public String getE_title() {
		return e_title;
	}
	public void setE_title(String e_title) {
		this.e_title = e_title;
	}
	public String getE_beginningDay() {
		return e_beginningDay;
	}
	public void setE_beginningDay(String e_beginningDay) {
		this.e_beginningDay = e_beginningDay;
	}
	public String getE_endDay() {
		return e_endDay;
	}
	public void setE_endDay(String e_endDay) {
		this.e_endDay = e_endDay;
	}
	
	
}
